class AutopilotTemplate(BaseOrgModel):
    """
    Reusable template for quickly creating reminders, rules, or campaigns.

    System templates (is_system=True) are seeded and shared across all orgs.
    Custom templates are org-scoped.
    """

    name = models.CharField(max_length=255)
    CATEGORIES = [
        ("cobranca", "Cobrança"),
        ("follow_up", "Follow-up"),
        ("onboarding", "Onboarding"),
        ("sla", "SLA"),
        ("nurture", "Nutrição"),
        ("operational", "Operacional"),
    ]
    category = models.CharField(max_length=50, choices=CATEGORIES)

    module_key = models.CharField(max_length=50)

    TEMPLATE_TYPES = [
        ("reminder", "Lembrete"),
        ("rule", "Regra"),
        ("campaign", "Campanha"),
    ]
    template_type = models.CharField(max_length=30, choices=TEMPLATE_TYPES)

    config_template = models.JSONField(default=dict)
    message_template = models.TextField(blank=True, default="")
    is_system = models.BooleanField(default=False)

    class Meta:
        db_table = "assistant_autopilot_template"
        verbose_name = "Autopilot Template"
        verbose_name_plural = "Autopilot Templates"

    def __str__(self):
        return f"{self.name} ({self.template_type})"
